def char_to_tile(c):
    if 'A' <= c <= 'Z': return TL_FONT_A + (ord(c) - ord('A'))
    if '0' <= c <= '9': return TL_DIGIT_0 + (ord(c) - ord('0'))
    return {' ':TL_FONT_SP, '.':TL_FONT_DOT, '!':TL_FONT_BANG,
            '?':TL_FONT_QM, ',':TL_FONT_COMMA, "'":TL_FONT_APOS,
            '-':TL_FONT_DASH, ':':TL_FONT_COLON}.get(c, TL_FONT_SP)

