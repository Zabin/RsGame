def build_tile_data():
    data = bytearray(256 * 16)
    def put(idx, tile):
        for i, b in enumerate(tile):
            data[idx * 16 + i] = b

    # OBJ
    put(TL_BUNNY_T_F1, bunny_top_f1())
    put(TL_BUNNY_B_F1, bunny_bot_f1())
