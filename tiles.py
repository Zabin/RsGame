# ── encoding helper ────────────────────────────────────────────
def enc(pix):
    """8x8 pixel array (values 0-3) → 16-byte 2bpp tile."""
    out = []
    for row in pix:
        p0 = p1 = 0
        for b, c in enumerate(row):
            if c & 1: p0 |= 0x80 >> b
            if c & 2: p1 |= 0x80 >> b
        out += [p0, p1]
    return out

